const {Schema, model} = require('mongoose');

const userSchema = new Schema({
    first_name: {
        type: String,
        required: true,
    },
    last_name: {
        type: String,
        required: true,
    },
    doc_id: {
        type: String,
        required: true,
    },
    phone: {
        type: Number,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    username: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    state: {
        type: Number,
        required: true,
    },

},{
    timestamps: true,
    versionKey: false
});

module.exports = model('User', userSchema);
